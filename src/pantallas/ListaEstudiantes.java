    private void buscar() {
        ListaEstudiantes.removeAllElements();
        for (Estudiante per : estudiantes) {
            if (per.getTexto().toLowerCase()
                    .contains(txtBuscar.getText().toLowerCase())) {
                ListaEstudiantes.addElement(per.getTexto());
            }
        }
    }
