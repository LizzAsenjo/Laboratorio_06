    public void AgregarEstudiante(Estudiante estud){
        estudiantes.add(estud);
        ListaEstudiantes.addElement(estud.getTexto());
        lstEstudiantes.setModel(ListaEstudiantes);
        ActualizarLista();
        return;
    }
    public void EditarEstudiante(Estudiante estud){
        
       Estudiante estudAnterior = (estud);
       estudAnterior.setNombre(
       estud.getNombre());
       estudAnterior.setCodigo(
       estud.getCodigo());
       ActualizarLista();
    }
    
    public void ActualizarLista(){
        
        ListaEstudiantes.removeAllElements();
        for(Estudiante estud: this.estudiantes){
            ListaEstudiantes.addElement(
                        estud.getTexto());  //
        }
        lstEstudiantes.setModel(ListaEstudiantes);
        
    }
     private void Cerrar() {
        this.padre.setVisible(true);
        this.dispose();
     }
