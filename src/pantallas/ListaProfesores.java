    public void ActualizarLista(){
        
        ListaProfesores.removeAllElements();
        for(Profesor prof: this.profesores){          //Lista
            ListaProfesores.addElement(
                        prof.getTexto());    //Agrega a la lista a cada profesor
        }
        lstProfesores.setModel(ListaProfesores);
        
    }
    
    public void AgregarProfesor(Profesor prof){
        profesores.add(prof);
        lstProfesores.setModel(ListaProfesores);
        ActualizarLista();
        return;
        
    }
