    private void buscar() {
        ListaProfesores.removeAllElements();
        for (Profesor per : profesores) {
            if (per.getTexto().toLowerCase()  //Minuscula
                    .contains(txtBuscar.getText().toLowerCase())) {
                ListaProfesores.addElement(per.getTexto());
            }
        }
    }
    
